#include "libft.h"

void	ft_lstadd_back(t_list **alst, t_list *new)
{
	t_list	*tmp;

	if (*alst == NULL)
		*alst = new;
	tmp = ft_lstlast(*(alst));
	tmp->next = new;
	tmp->next->next = NULL;
}
